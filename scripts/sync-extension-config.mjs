/**
 * Reads VITE_SUPABASE_* from .env and writes extension/lib/supabase-defaults.js
 * Run: npm run extension:config
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = resolve(root, ".env");
const outPath = resolve(root, "extension/lib/supabase-defaults.js");

function parseEnv(text) {
  const out = {};
  for (const line of text.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq < 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

let env = {};
try {
  env = parseEnv(readFileSync(envPath, "utf8"));
} catch {
  console.warn("No .env found — writing empty defaults.");
}

const url = env.VITE_SUPABASE_URL || "";
const anonKey = env.VITE_SUPABASE_ANON_KEY || "";

const contents = `/** Auto-generated by scripts/sync-extension-config.mjs — do not edit by hand */
export const DEFAULT_SUPABASE_URL = ${JSON.stringify(url)};
export const DEFAULT_SUPABASE_ANON_KEY = ${JSON.stringify(anonKey)};
`;

writeFileSync(outPath, contents, "utf8");
console.log(`Wrote ${outPath}`);
if (!url || !anonKey) {
  console.warn("Warning: VITE_SUPABASE_URL or VITE_SUPABASE_ANON_KEY missing in .env");
}
